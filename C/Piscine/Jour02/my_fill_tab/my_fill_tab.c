int            *my_fill_tab()
{
    static int tab[99] = { 0 };
    int        rank;
    int        value;

    rank = 0;
    value = 1;
    while (value <= 100)
    {
        tab[rank] = value;
        ++value;
        ++rank;
    }
    return (tab);
}
